/*
-> Math Object: Math Object is a in built Object in JS which is used to
perform Mathematical Operations.
-> Inbuilt method object Methods:
1. Math.PI
2. Math.sqrt()
3. Math.pow(a,b)
4. Math.round()
5. Math.floor()
6. Math.trunc()
7. Math.sign()
8. Math.random()
*/

console.log(Math.PI) //prints the value of PI
console.log(Math.sqrt(94)) //prints the sqrt of number
console.log(Math.pow(60,3)) //print the power of a number
console.log(Math.round(3.3)) //round offs to the nearest value
console.log(Math.floor(4.9))  //floor it to the nearest value
console.log(Math.trunc(127.999)) //It removes the values after decimal point
console.log(Math.sign(129)) //If value > 1 --> 1 and value < 1 -->-1
console.log(Math.random()) //prints the random numbers

console.log(Math.trunc(Math.random()*6+1))  //Generate random numbers from 1 to 6



